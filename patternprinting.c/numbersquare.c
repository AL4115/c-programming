//number square 
# include <stdio.h>
int main(){
    int n;
    printf("enter the no. of rows:");
    scanf("%d",&n);
    int m;
    printf("enter the no. of columns :");
    scanf("%d",&m);
    for(int i=1;i<=n;i++){
        for(int j=1;j<=m;j++){
            printf("%d",j);
        }
        printf("\n");
    };
    return 0;
}